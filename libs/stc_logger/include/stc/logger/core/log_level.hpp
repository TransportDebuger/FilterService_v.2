#pragma once

namespace stc::logger {

/**
 * @enum LogLevel
 * @brief Иерархия уровней важности сообщений лога.
 */
enum class LogLevel {
  kTrace,  ///< Максимально детализированная диагностическая информация.
  kDebug,  ///< Отладочная информация для разработки и тестирования.
  kInfo,  ///< Информация о нормальном ходе выполнения программы.
  kWarning,  ///< Предупреждения о потенциальных проблемах.
  kError,  ///< Ошибки выполнения, не приводящие к краху приложения.
  kCritical  ///< Критические ошибки, делающие дальнейшее выполнение
             ///< невозможным.
};

}  // namespace stc::logger